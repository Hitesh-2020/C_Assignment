//2.	Number pattern programs - Write a C program to print the given number patterns
//    a) Square number patterns

#include <stdio.h>

int main()
{     int n=5;

      for(int i=1;i<=n;i++)
      {
            for(int j=1;j<=n;j++)
            {
                  printf("1");
            }

            printf("\n");
      }

      return 0;
}
